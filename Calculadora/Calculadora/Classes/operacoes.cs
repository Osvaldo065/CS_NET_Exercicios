namespace Calculadora.Classes;

 public class Operações{

    //Metodo para somar
    public static float Somar(float numUm, float numDois)
    {
        return numUm + numDois;
    }

    //Metodo para subtrair 
    public static float Subtracao(float numUm, float numDois)
    {
        return numUm - numDois;
    }

    //metodo para dividir
    public static float Divisao(float numUm, float numDois)
    {
        return numUm / numDois;
    }

    //metodo para multiplicar
    public static float Multiplicacao(float numUm, float numDois)
    {
        return numUm * numDois;
    }
    
}